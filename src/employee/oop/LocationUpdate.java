    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        String id = EmployeeID.getText();
        String newloc = NewLocation.getText();

        // flags for performing submission
        boolean idcheck, newloccheck;
        idcheck = newloccheck = false;

        // Validate the fields value
        if (id.isBlank()) {
            EmployeeID.setText("Please enter an ID.");
            EmployeeID.setForeground(Color.red);
        } else {
            idcheck = true;
        }

        if (newloc.isBlank()) {
            NewLocationMsg.setText("Please enter a new location.");
            NewLocationMsg.setForeground(Color.red);
        } else if (newloc.length() < 3) {
            NewLocationMsg.setText("Location name must have at least 3 characters.");
            NewLocationMsg.setForeground(Color.red);
        } else if (newloc.length() > 5) {
            NewLocationMsg.setText("Location name cannot exceed 5 characters.");
            NewLocationMsg.setForeground(Color.red);
        } else {
            newloccheck = true;
        }

        // update code only executes when both flags = true
        if (idcheck && newloccheck) {
            myConnection();
            String query = "Update Employee set EmployeeLocation = ? where EmployeeID = ?";
            try {
                PreparedStatement statement = conn.prepareStatement(query);
                statement.setString(1, NewLocation.getText());
                statement.setString(2, EmployeeID.getText());
                statement.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Location Successfully Updated!");
                EmployeeMM screen = new EmployeeMM();
                screen.setVisible(true);
                setVisible(false);
                conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(LocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Update Unsuccessful.\n"
                    + "Check the field message for more info.");
        }
    }//GEN-LAST:event_SubmitActionPerformed

