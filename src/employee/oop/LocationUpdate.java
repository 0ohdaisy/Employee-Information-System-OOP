    private void EmployeeIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmployeeIDActionPerformed
        // for displaying info associated with the id (press enter)
        myConnection();
        String query = "Select EmployeeName, EmployeeLocation from Employee "+ "WHERE EmployeeID = '" + EmployeeID.getText() + "'";
        
        // Checking if id is legit
        if (EmployeeID.getText().length() != 4 ){
            JOptionPane.showMessageDialog(rootPane, "Invalid ID. Please" + " reenter.");
                    EmployeeIDMsg.setText("ID has 4 digits.");
                    EmployeeIDMsg.setForeground(Color.RED);
        } else {
            try {
                PreparedStatement statement = conn.prepareStatement(query);
                ResultSet rs = statement.executeQuery(query);
                if (rs.next()){
                    EmployeeName.setText(rs.getString("EmployeeName"));
                    Location.setText(rs.getString("EmployeeLocation"));
                    EmployeeIDMsg.setText("ID accepted.");
                    EmployeeIDMsg.setForeground(Color.BLUE);
                    EmployeeID.setEnabled(false);
                    NewLocation.setEnabled(true);
                }
                else {
                    JOptionPane.showMessageDialog(rootPane, "ID does not exist. " + "Please reenter.");
                    EmployeeIDMsg.setText("ID does not exist.");
                    EmployeeIDMsg.setForeground(Color.RED);
                }
            } catch (SQLException ex) {
                Logger.getLogger(LocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
    }//GEN-LAST:event_EmployeeIDActionPerformed

    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        myConnection();
        try {
            String sql = "select * from Employee where EmployeeID=?";
            pstmt = conn.prepareStatement(sql);
            int id = Integer.parseInt(EmployeeID.getText());
            pstmt.setInt(1, id);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                EmployeeID.setText(rs.getString("EmployeeID"));
                EmployeeName.setText(rs.getString("EmployeeName"));
                Location.setText(rs.getString("EmployeeLocation"));
            } else if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "ID doesn't exist");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SearchActionPerformed

