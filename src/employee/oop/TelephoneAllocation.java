    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // update new allocated number
        MyConnection();
        String EmployeeTelNum = TelephoneNumber.getText();
        String ID = EmployeeID.getText();

        //ask the choice from user
        int allocation = JOptionPane.showConfirmDialog(this, "Telephone number is already allocated. Do you want to allocate a new number?", "Yes", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (allocation == JOptionPane.YES_OPTION) {

            String sql = "UPDATE Employee SET EmployeeTelNum = ? where EmployeeID = ?";
            try {
                st = connection.prepareStatement(sql);
                st.setString(1, EmployeeTelNum);
                st.setString(2, ID);

                if (TelephoneNumber.getText().length() < 7) { //to check if user input a seven digit telephone number
                    TelephoneNumber.setText("Number must have at least 7 digits.");
                } else {
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "New number allocated");
                    TelephoneMM frame = new TelephoneMM();
                    frame.setVisible(true);
                    setVisible(false);
                }
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(TelephoneAllocation.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_UpdateActionPerformed

