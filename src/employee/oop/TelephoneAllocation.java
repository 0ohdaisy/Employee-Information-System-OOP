    private void EnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterActionPerformed
        // To display Employee ID, Employee Location and Employee Department
        MyConnection();
        try {
            String sql = "select * from Employee, Department where EmployeeID = ?";
            st = connection.prepareStatement(sql);
            int id = Integer.parseInt(EmployeeID.getText());
            st.setInt(1, id);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                EmployeeID.setText(rs.getString("EmployeeID"));
                Location.setText(rs.getString("EmployeeLocation"));
                DepartName.setText(rs.getString("DepartName"));
            } else if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Employee Table for Employee ID is empty");
            }

        } catch (SQLException ex) {
            Logger.getLogger(TelephoneAllocation.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_EnterActionPerformed

