    // generate department code by the system start with 1000
    public void autoGenerate() {
        myConnection();

        try {
            Statement st = conn.createStatement();

            //getting the department code from database
            String sql = "select count(DepartCode) from Department";
            st = conn.prepareStatement(sql);
            rs = st.executeQuery(sql);
            if (rs.next()) {
                int sum = rs.getInt("count(DepartCode)");
                int count = sum + 1000;
                DepartCode.setText("" + count);
            }

        } catch (SQLException ex) {
            Logger.getLogger(AddDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

