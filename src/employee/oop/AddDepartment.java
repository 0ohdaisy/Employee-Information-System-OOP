    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // Connection from database
        myConnection();

        //get the name and code
        String name = DepartName.getText();
        String generatecode = DepartCode.getText();

        //insert to department database table 
        try {
            String SQL = "INSERT INTO Department VALUES( ?,?)";
            pstmt = conn.prepareStatement(SQL);
            pstmt.setString(1, generatecode);
            pstmt.setString(2, name);

            //checking for validation department name
            if (name.matches("^[A-Z][A-Za-z-&\\s]{2,14}$") == true) {

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Department Successfully Added");
                new DepartmentMM().setVisible(true);
                dispose();
                conn.close();
            }

        } catch (SQLException ex) {
            Logger.getLogger(DepartmentUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SubmitActionPerformed

